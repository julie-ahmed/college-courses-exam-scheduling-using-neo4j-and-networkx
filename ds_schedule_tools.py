from neo4j import GraphDatabase

class GraphToNeo4j:
    def __init__(self, uri, user, password):
        self.driver = GraphDatabase.driver(uri, auth=(user, password))

    def close(self):
        self.driver.close()

    def create_node(self, tx, node_id, color):
        tx.run("""
            MERGE (n:Node {id: $id})
            SET n.color = $color
        """, id=node_id, color=color)

    def create_edge(self, tx, source, target):
        tx.run("""
            MATCH (a:Node {id: $source})
            MATCH (b:Node {id: $target})
            MERGE (a)-[:CONNECTED_TO]->(b)
        """, source=source, target=target)

    def upload_graph(self, graph):
        with self.driver.session() as session:
            for node_id, attrs in graph.nodes(data=True):
                color = attrs.get('color', 'unknown')
                session.execute_write(self.create_node, node_id, color)

            for source, target in graph.edges():
                session.write_transaction(self.create_edge, source, target)

