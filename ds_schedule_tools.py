def greedy_colouring_algorithm(courses_network, colour_list):
  nodes=list(courses_network.nodes())
  rnd.shuffle(nodes)
  coloured_nodes={}
  for node in nodes:
    node_neighbors=list(dict(courses_network[node]).keys())       #This view is an iterable (not a list), but it can be converted into a list.
    forbidden_colours_of_every_node=[]
    for this_neighbor in node_neighbors :
      if this_neighbor in coloured_nodes:
        forbidden_colours_of_every_node.append(coloured_nodes[this_neighbor])
    for colour in colour_list:
      if colour not in forbidden_colours_of_every_node:
        coloured_nodes[node]=colour
        break
  return coloured_nodes

